module num_led(
    input clk,
    input rst,
    input [3:0]num,
    output reg [15:0]led
    );
    
    always @* begin
        case(num)
            4'd0: led = 16'b0000000000000001;
            4'd1: led = 16'b0000000000000010;
            4'd2: led = 16'b0000000000000100;
            4'd3: led = 16'b0000000000001000;
            4'd4: led = 16'b0000000000010000;
            4'd5: led = 16'b0000000000100000;
            4'd6: led = 16'b0000000001000000;
            4'd7: led = 16'b0000000010000000;
            4'd8: led = 16'b0000000100000000;
            4'd9: led = 16'b0000001000000000;
            4'd10: led = 16'b0000010000000000;
            4'd11: led = 16'b0000100000000000;
            4'd12: led = 16'b0001000000000000;
            4'd13: led = 16'b0010000000000000;
            4'd14: led = 16'b0100000000000000;
            4'd15: led = 16'b1000000000000000;
        endcase
    end
    
endmodule
